#!/usr/bin/env node
/**
 * Generates src/assets/bank-logos.ts from preview/public/bank-images/
 * so logos work in host projects without requiring /bank-images/ to be served.
 */
import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from "fs";
import { join, dirname, extname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const imagesDir = join(root, "preview", "public", "bank-images");
const outPath = join(root, "src", "assets", "bank-logos.ts");

const MIME_BY_EXT = {
  ".png": "image/png",
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".svg": "image/svg+xml",
};

function getMime(filename) {
  const ext = extname(filename).toLowerCase();
  return MIME_BY_EXT[ext] ?? "application/octet-stream";
}

function main() {
  if (!existsSync(imagesDir)) {
    console.error(`Images directory not found: ${imagesDir}`);
    process.exit(1);
  }

  const files = readdirSync(imagesDir).filter(
    (f) => f.endsWith(".png") || f.endsWith(".jpg") || f.endsWith(".jpeg") || f.endsWith(".svg")
  );

  const entries = [];
  for (const file of files) {
    const filePath = join(imagesDir, file);
    const buf = readFileSync(filePath);
    const base64 = buf.toString("base64");
    const mime = getMime(file);
    const dataUrl = `data:${mime};base64,${base64}`;
    entries.push(`  "${file}": ${JSON.stringify(dataUrl)}`);
  }

  mkdirSync(dirname(outPath), { recursive: true });

  const ts = `/* eslint-disable */
// Auto-generated by scripts/generate-bank-logos.js - do not edit
export const BANK_LOGO_DATA_URLS: Record<string, string> = {
${entries.join(",\n")},
};
`;

  writeFileSync(outPath, ts, "utf-8");
  console.log(`Generated ${outPath} (${files.length} logos)`);
}

main();
