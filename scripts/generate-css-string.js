#!/usr/bin/env node
/**
 * Generates src/styles/bank-ui.css.ts from src/styles/bank-ui.css
 * so the CSS can be imported as a string for Shadow DOM injection.
 */
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const cssPath = join(root, "src/styles/bank-ui.css");
const outPath = join(root, "src/styles/bank-ui-css.ts");

const css = readFileSync(cssPath, "utf-8");
const escaped = css
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\${/g, "\\${");

const ts = `/* eslint-disable */
// Auto-generated by scripts/generate-css-string.js - do not edit
export const bankUiCss = \`${escaped}\`;
`;

writeFileSync(outPath, ts, "utf-8");
console.log("Generated src/styles/bank-ui-css.ts");
